import UIKit

extension UIColor {
    
    static func tipsterDarkGreen() -> UIColor {
        return  UIColor(red:0.26, green:0.49, blue:0.36, alpha:1.0)
    }
    
    static func tipsterLightGreen() -> UIColor {
        return  UIColor(red:0.50, green:0.85, blue:0.53, alpha:1.0)
    }
}